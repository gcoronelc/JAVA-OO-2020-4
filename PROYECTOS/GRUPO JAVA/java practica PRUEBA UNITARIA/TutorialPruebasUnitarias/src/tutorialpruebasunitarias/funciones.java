
package tutorialpruebasunitarias;


public class funciones {
    //FUNCION SUMAR
    public int sumar(int a, int b){
        return a+b;
    }
    //FUNCION RESTAR
    public int restar(int a, int b){
        return a-b;
    }
    //FUNCION MULTIPLICAR
    public int miltiplicar(int a, int b){
        return a*b;
    }
    //FUNCION DIVIDIR
    public int dividir(int a, int b){
        return a/b;
    }
    
    
}
