package ne.pc.calculadora.Service;
public class Service {
    public double calcSuma(double dato1,double dato2){
        double suma=dato1+dato2;
        return suma;
    }
    public double calcResta(double dato1,double dato2){
        double resta=dato1-dato2;
        return resta;
    }
    public double calcMulti(double dato1,double dato2){
        double multi=dato1*dato2;
        return multi;
    }
    public double calcDiv(double dato1,double dato2){
        double div=dato1/dato2;
        return div;
    }
}
