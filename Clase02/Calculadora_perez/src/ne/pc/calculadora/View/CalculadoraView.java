    private void btnSumaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumaActionPerformed
        btnSuma.setEnabled(false);
        btnDiv.setEnabled(false);
        btnMulti.setEnabled(false);
        btnResta.setEnabled(false);
        btnReinicio.setEnabled(true);
        txtNum1.setEnabled(false);
        txtNum2.setEnabled(false);
        double dato1,dato2,suma;
        dato1=Double.parseDouble(txtNum1.getText());
        dato2=Double.parseDouble(txtNum2.getText());
        Service service=new Service();
        suma=service.calcSuma(dato1, dato2);
        txtOpera.setText("Suma");
        txtResult.setText(""+suma);
    }//GEN-LAST:event_btnSumaActionPerformed

